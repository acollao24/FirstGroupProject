package com.syntax.class01;

public class PracticeVariables {

	public static void main(String[] args) {
		// number values
		byte danny = 1;
		byte dannyy = 2;
		System.out.println(danny);
		System.out.println(dannyy);

		short cat = 24;
		System.out.println(cat);

		int thiago = 26;
		System.out.println(thiago);
		int daniel = 25;
		System.out.println(daniel);

		long fairfax = 1000;
		System.out.println(fairfax);
		
		// decimal values
		float box1 = 29;
		System.out.println(box1);
		double hair= 500;
		System.out.println(hair);
		
		// single character
		char blonde= 'A';
		System.out.println(blonde);
		
		// true or false
		boolean yes=true;
		boolean no=false;
		System.out.println(yes);
		System.out.println(no);
		

	}

}
